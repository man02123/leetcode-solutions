class Solution {
  public:
    string armstrongNumber(int n){
        int x=n;
        int k=0;
        
        while(n)
        {
           int r=n%10;
           k+=pow(r,3);
           n=n/10;
        }
        if(k==x)
        return "Yes";
        
        return "No";
    }
};

